int	ft_iterative_power(int nb, int power)
{
	int	result;

	result = nb;
	while (1 < power)
	{
		result *= nb;
		power--;
	}
	if (power == 0)
	{
		return (1);
	}
	if (power < 0)
	{
		return (0);
	}
	return (result);
}
