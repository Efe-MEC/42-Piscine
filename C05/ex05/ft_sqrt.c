int	ft_sqrt(int nb)
{
	long	i;
	long	a;

	a = nb;
	if (a <= 0)
	{
		return (0);
	}
	if (a == 1)
	{
		return (1);
	}
	i = 2;
	if (a >= 2)
	{
		while (i * i <= a)
		{
			if (i * i == a)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
